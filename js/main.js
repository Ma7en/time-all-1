/* start time 1 */
let t1hr = document.querySelector(".time-1 #hr");
let t1mn = document.querySelector(".time-1 #mn");
let t1sc = document.querySelector(".time-1 #sc");

setInterval(() => {
    let t1day = new Date();
    let t1hh = t1day.getHours() * 30;
    let t1mm = t1day.getMinutes() * 6;
    let t1ss = t1day.getSeconds() * 6;

    t1hr.style.transform = `rotateZ(${t1hh + (t1mm / 12)}deg)`;
    t1mn.style.transform = `rotateZ(${t1mm}deg)`;
    t1sc.style.transform = `rotateZ(${t1ss}deg)`;


    // digital clock
    let t1hours = document.querySelector(".time-1 #hours");
    let t1minutes = document.querySelector(".time-1 #minutes");
    let t1seconds = document.querySelector(".time-1 #seconds");
    let t1ampm = document.querySelector(".time-1 #ampm");

    let t1h = new Date().getHours();
    let t1m = new Date().getMinutes();
    let t1s = new Date().getSeconds();

    let t1am = t1h >= 12 ? "PM" : "AM"

    // convert 24hr clock to 12hr clock
    if(t1h > 12) {
        t1h = t1h - 12;
    }

    // add zero before single digit number
    t1h = (t1h < 10 ) ? "0" + t1h : t1h
    t1m = (t1m < 10 ) ? "0" + t1m : t1m
    t1s = (t1s < 10 ) ? "0" + t1s : t1s

    t1hours.innerHTML = t1h;
    t1minutes.innerHTML = t1m;
    t1seconds.innerHTML = t1s;
    t1ampm.innerHTML = t1am;


});





/* end time 1 */

